    public void guardar() {
        String text = txtCurso.getText();
        ma_paralelo mp = new ma_paralelo();
        mp.setParalelo(text);
        mp.setId_actualizacion(Long.valueOf(lbIdUsuario.getText()));
        String a = crud.crearCursos(mp);
        JOptionPane.showMessageDialog(this, a);
        setVisible(false);
    }
